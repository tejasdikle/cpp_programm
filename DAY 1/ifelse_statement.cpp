#include<iostream>
int main ()
{
    int num ;
    std :: cout << "Enter the number :";
    std :: cin >> num;
    
    
    if (num == 0)
    {
        std :: cout << "Zero";
    }
    else if (num == 1)
    {
        std :: cout << "One";    
    }
    else if (num == 2)
    {
        std ::cout << "Two";
    }
    else if (num == 3)
    {
        std :: cout << "Three";
    }
    else if (num == 4)
    {
        std :: cout << "Four";
    }
    else if(num == 5)
    {
        std :: cout << "Five ";
    }
    else if (num == 6)
    {
        std :: cout << "Six";
    }
    else if (num == 7)
    {
        std :: cout << "Seven";
    }
    else if (num == 8)
    {
        std :: cout << "Eight ";
    }
    else if (num == 9)
    {
        std :: cout << "Nine ";
    }  
    else 
    {
        std :: cout << "Invalid number";
    }
}